	ret
	ENDW ISDELIM

	DEFW TOKEN, "TOKEN"
	# ( -- )
	RPUSH x1
	LIT 0
	FC WIN, STORE
1:
	FC KEY, DUP, ISDELIM

	ZBR 3f
	FC WIN, LOAD
	ZBR 2f
