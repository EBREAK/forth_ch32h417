	andi x15, x15, NLEN_MASK
	ret
	ENDW XT2NLEN

	DEFW WORDLIST_WORDS, "WORDLIST-WORDS"
	# ( NT -- )
	RPUSH x1
	FC DUP
	ZBR 2f
1:
	FC SPACE, DUP, NT2NAME, OVER, NT2NLEN, TYPE
	FC NT2NEXT, DUP, EQZ
	ZBR 1b
2:
