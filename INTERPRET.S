	DEFW CONTEXT_LIMIT, "CONTEXT-LIMIT"
	LIT CONTEXT_LIMIT
	ret
	ENDW CONTEXT_LIMIT

	DEFW CONTEXT, "CONTEXT"
	.PUSHSECTION .data
	.P2ALIGN 2, 0
CONTEXT:
	.FILL CONTEXT_LIMIT, 4, 0
	.POPSECTION
	LAT CONTEXT
	ret
	ENDW CONTEXT

	DEFW NT2NEXT, "NT>NEXT"
	bnez x15, 1f
	ret
1:
	lbu x14, 0(x15)
	lbu x13, 1(x15)
	slli x13, x13, 8
	or x14, x14, x13
	bnez x14, 1f
	li x15, 0
	ret
1:
	andi x14, x14, NLEN_MASK
	add x15, x15, x14
	addi x15, x15, 1 + 4
	lbu x14, 0(x15)
	lbu x13, 1(x15)
	slli x13, x13, 8
	or x14, x14, x13
	bnez x14, 1f
	li x15, 0
1:
	ret
	ENDW NT2NEXT

	DEFW NT2NAME, "NT>NAME"
