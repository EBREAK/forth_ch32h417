	slli x15, x15, 2
	FC CONTEXT, PLUS, LOAD, WORDLIST_WORDS
	addi x15, x15, 1
	FC DUP, CONTEXT_LIMIT, EQ
	ZBR 1b
	RPOP x1
	TC DROP
	ENDW WORDS

	DEFW WORDLIST_FIND, "WORDLIST-FIND"
	# ( C-ADDR COUNT NT -- 0|NT )
	RPUSH x1
	FC 2DUP, EQZ, SWAP, EQZ, OR, INVERT
	ZBR 3f
1:
	FC 2DUP, NT2NLEN, EQ
	ZBR 2f
	FC DUP, NT2NAME, 2OVER, MEMCMP, EQZ
	ZBR 2f
	FC NIP
	RPOP x1
	TC NIP
2:
	FC NT2NEXT, DUP, EQZ
	ZBR 1b
3:
	FC 3DROP
	LIT 0
	RPOP x1
	ret
	ENDW WORDLIST_FIND

	DEFW FIND, "FIND"
	# ( C-ADDR COUNT -- 0|NT )
	RPUSH x1
	LIT 0
1:
	FC DUP
	slli x15, x15, 2
	FC CONTEXT, PLUS, LOAD, 2OVER, ROT, WORDLIST_FIND
	FC DUP, NEZ
	ZBR 2f
