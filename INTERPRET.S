	lbu x14, 0(x15)
	andi x14, x14, NLEN_MASK
	addi x14, x14, 1
	add x14, x14, x15
	lbu x15, 0(x14)
	lbu x13, 1(x14)
	slli x13, x13, 8
	or x15, x15, x13
	lbu x13, 2(x14)
	slli x13, x13, 16
	or x15, x15, x13
	lbu x13, 3(x14)
