	ENDW WIB_PUSH

	DEFW ISDELIM, "DELIM?"
	# ( C -- FLAG )
	xori x14, x15, '\r'
	beqz x14, 1f
	xori x14, x15, '\n'
	beqz x14, 1f
	xori x14, x15, ' '
