	or x15, x15, x13
	ret
	ENDW NT2CODE

	DEFW REPL, "REPL"
	FC TOKEN, WIB, WIN, LOAD, FIND, DUP
	ZBR REPL_TRY_NUMBER
REPL_WORD_FOUND:
	FC NT2CODE, EXECUTE
	j REPL_STACK_CHK
REPL_TRY_NUMBER:
REPL_NOT_FOUND:
	FC DROP, CR, WIB, WIN, LOAD, TYPE, SPACE
	.PUSHSECTION .rodata
MSG_NOT_FOUND:
	.ASCII "NOT FOUND\r\n"
	.SET LEN_MSG_NOT_FOUND, . - MSG_NOT_FOUND
	.POPSECTION
	LAT MSG_NOT_FOUND
	LIT LEN_MSG_NOT_FOUND
	FC TYPE
	TC ABORT
REPL_STACK_CHK:
	lw x14, USER_PS0(x4)
	blt x14, x9, REPL_STACK_ERROR
	addi x14, x14, -(STACK_DEPTH * 4)
	bge x14, x9, REPL_STACK_ERROR
	lw x14, USER_RS0(x4)
	blt x14, x9, REPL_STACK_ERROR
	addi x14, x14, -(STACK_DEPTH * 4)
	bge x14, x8, REPL_STACK_ERROR
	TC REPL
REPL_STACK_ERROR:
	li x15, TOS_INIT
	lw x9, USER_PS0(x4)
	lw x8, USER_RS0(x4)
	.PUSHSECTION .rodata
MSG_STACK_ERROR:
	.ASCII "\r\nSTACK ERROR\r\n"
	.SET LEN_MSG_STACK_ERROR, . - MSG_STACK_ERROR
	.POPSECTION
	LAT MSG_STACK_ERROR
	LIT LEN_MSG_STACK_ERROR
	FC TYPE
	TC ABORT
	ENDW REPL
