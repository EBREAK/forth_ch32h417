TEST_CONTEXT_LIMIT:
	LIT CONTEXT_LIMIT
	FC CONTEXT_LIMIT, EQCHK, PZCHK

TEST_CONTEXT:
	LAT CONTEXT
	FC CONTEXT, EQCHK, PZCHK

TEST_NT2NEXT:
	LIT 0, 0
	FC NT2NEXT, EQCHK
	LIT 0
	FC CONTEXT, LOAD, NT2NEXT, EQCHK
	FC CONTEXT
	addi x15, x15, 4
	FC LOAD, NT2NEXT, NEZ, TRUE, EQCHK
	LAT NT_CONTEXT, NT_CONTEXT_LIMIT
	FC NT2NEXT, EQCHK
	LIT 0
	LAT NT_ROM_LATEST
	FC NT2NEXT, EQCHK, PZCHK

TEST_NT2NAME:
	LAT NAME_NOOP, NT_NOOP
	FC NT2NAME, EQCHK
	LAT NAME_CONTEXT, NT_CONTEXT
	FC NT2NAME, EQCHK, PZCHK

TEST_NT2NLEN:
	LIT 0, 0
	FC NT2NLEN, EQCHK
	LIT 4
	LAT NT_NOOP
	FC NT2NLEN, EQCHK
	LIT 1
	LAT NT_LOAD
	FC NT2NLEN, EQCHK, PZCHK

TEST_WORDLIST_WORDS:
	LIT 0
	FC WORDLIST_WORDS, PZCHK
	.PUSHSECTION .text.unused
	FC CONTEXT, LOAD, WORDLIST_WORDS, PZCHK
	FC CONTEXT
	addi x15, x15, 4
	FC LOAD, WORDLIST_WORDS, PZCHK
	.POPSECTION

TEST_WORDS:
	.PUSHSECTION .text.unused
	FC WORDS, PZCHK
	.POPSECTION
