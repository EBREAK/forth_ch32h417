TEST_CONTEXT_LIMIT:
	LIT CONTEXT_LIMIT
	FC CONTEXT_LIMIT, EQCHK, PZCHK

TEST_CONTEXT:
	LAT CONTEXT
	FC CONTEXT, EQCHK, PZCHK

TEST_NT2NEXT:
	LIT 0, 0
	FC NT2NEXT, EQCHK
	LIT 0
	FC CONTEXT, LOAD, NT2NEXT, EQCHK
	FC CONTEXT
	addi x15, x15, 4
	FC LOAD, NT2NEXT, NEZ, TRUE, EQCHK
	LAT NT_CONTEXT, NT_CONTEXT_LIMIT
	FC NT2NEXT, EQCHK
	LIT 0
	LAT NT_ROM_LATEST
	FC NT2NEXT, EQCHK, PZCHK

TEST_NT2NAME:
	LAT NAME_NOOP, NT_NOOP
	FC NT2NAME, EQCHK
	LAT NAME_CONTEXT, NT_CONTEXT
	FC NT2NAME, EQCHK, PZCHK

TEST_NT2NLEN:
	LIT 0, 0
	FC NT2NLEN, EQCHK
	LIT 4
	LAT NT_NOOP
	FC NT2NLEN, EQCHK
	LIT 1
	LAT NT_LOAD
	FC NT2NLEN, EQCHK, PZCHK

TEST_WORDLIST_WORDS:
	LIT 0
	FC WORDLIST_WORDS, PZCHK
	.PUSHSECTION .text.unused
	FC CONTEXT, LOAD, WORDLIST_WORDS, PZCHK
	FC CONTEXT
	addi x15, x15, 4
	FC LOAD, WORDLIST_WORDS, PZCHK
	.POPSECTION

TEST_WORDS:
	.PUSHSECTION .text.unused
	FC WORDS, PZCHK
	.POPSECTION

TEST_WORDLIST_FIND:
	LIT 0, 5, 0, 0
	FC WORDLIST_FIND, EQCHK
	LIT 0, 5, 1, 0
	FC WORDLIST_FIND, EQCHK
	LIT 0, 1, 999
	FC CONTEXT
	addi x15, x15, 4
	FC LOAD, WORDLIST_FIND, EQCHK
	LAT NT_LOAD, NAME_LOAD
	LIT NLEN_LOAD
	FC CONTEXT
	addi x15, x15, 4
	FC LOAD, WORDLIST_FIND, EQCHK
	LAT NT_CONTEXT, NAME_CONTEXT
	LIT NLEN_CONTEXT
	FC CONTEXT
	addi x15, x15, 4
	FC LOAD, WORDLIST_FIND, EQCHK
	LIT 0
	LAT NAME_CONTEXT
	LIT NLEN_CONTEXT + 2
	FC CONTEXT
	addi x15, x15, 4
	FC LOAD, WORDLIST_FIND, EQCHK, PZCHK

TEST_FIND:
	LIT 0, 0, 0
	FC FIND, EQCHK
	LIT 0, 0, 999
	FC FIND, EQCHK
	LAT NT_LOAD, NAME_LOAD
	LIT NLEN_LOAD
	FC FIND, EQCHK
	LAT NT_CONTEXT_LIMIT, NAME_CONTEXT_LIMIT
	LIT NLEN_CONTEXT_LIMIT
	FC FIND, EQCHK
	LIT 0
	LAT NAME_CONTEXT
	LIT NLEN_CONTEXT + 2
	FC FIND, EQCHK, PZCHK

TEST_WIB:
	FC WIN, LOAD, EQZ, TRUE, EQCHK
	LIT NLEN_MASK
	FC WIB_LIMIT, EQCHK, PZCHK

TEST_WIB_PUSH:
	LIT 0
	FC WIN, STORE
	LIT 1, 0x55
	FC DUP, WIB_PUSH, WIB, CLOAD, EQCHK
	FC WIN, LOAD, EQCHK
	LIT 2, 0xAA
	FC DUP, WIB_PUSH, WIB
	addi x15, x15, 1
	FC CLOAD, EQCHK
	FC WIN, LOAD, EQCHK
	FC WIB_LIMIT, WIN, STORE
	LIT 1, 0xFF
	FC DUP, WIB_PUSH, WIB, CLOAD, EQCHK
	FC WIN, LOAD, EQCHK, PZCHK

TEST_ISDELIM:
	LIT -1, '\r'
	FC ISDELIM, EQCHK
	LIT -1, '\n'
	FC ISDELIM, EQCHK
	LIT -1, ' '
	FC ISDELIM, EQCHK
	LIT 0, 'a'
	FC ISDELIM, EQCHK, PZCHK

TEST_TOKEN:
	.PUSHSECTION .text.unused
	FC TOKEN, WIB, WIN, LOAD, TYPE, PZCHK
	j TEST_TOKEN
	.POPSECTION
