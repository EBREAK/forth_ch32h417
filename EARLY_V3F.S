	.PUSHSECTION .text.early
EARLY_INIT:
	li x10, RCC_BASE
	# HSI 25Mhz
HSION:
	lw x11, R32_RCC_CTLR(x10)
	ori x11, x11, RCC_HSION
	sw x11, R32_RCC_CTLR(x10)
WAIT_HSIRDY:
	lw x11, R32_RCC_CTLR(x10)
	andi x11, x11, RCC_HSIRDY
	beqz x11, WAIT_HSIRDY

SET_FPRE:
	lw x11, R32_RCC_CFGR0(x10)
	li x12, ~(RCC_FPRE_MASK)
	and x11, x11, x12
	li x12, RCC_FPRE_4DIV
	or x11, x11, x12
	sw x11, R32_RCC_CFGR0(x10)

SET_HPRE:
	lw x11, R32_RCC_CFGR0(x10)
	andi x11, x11, ~(RCC_HPRE_MASK)
	ori x11, x11, RCC_HPRE_1DIV
	sw x11, R32_RCC_CFGR0(x10)

SET_SYSCLK_HSI:
	lw x11, R32_RCC_CFGR0(x10)
	andi x11, x11, ~(RCC_SW_MASK)
	ori x11, x11, RCC_SW_HSI
	sw x11, R32_RCC_CFGR0(x10)
WAIT_SYSCLK_HSI:
	lw x11, R32_RCC_CFGR0(x10)
	andi x11, x11, RCC_SWS_MASK
	xori x11, x11, RCC_SWS_HSI
	bnez x11, WAIT_SYSCLK_HSI

PLLOFF:
	lw x11, R32_RCC_CTLR(x10)
	li x12, ~(RCC_PLLON)
	and x11, x11, x12
	sw x11, R32_RCC_CTLR(x10)
WAIT_PLLOFF:
	lw x11, R32_RCC_CTLR(x10)
	li x12, RCC_PLLRDY
	and x11, x11, x12
	bnez x11, WAIT_PLLOFF

SYSPLL_GATE_OFF:
	lw x11, R32_RCC_PLLCFGR(x10)
	li x12, ~(RCC_SYSPLL_GATE)
	and x11, x11, x12
	sw x11, R32_RCC_PLLCFGR(x10)

SYSPLL_SRC_SEL:
	lw x11, R32_RCC_PLLCFGR(x10)
	li x12, ~(RCC_SYSPLL_SEL_MASK)
	and x11, x11, x12
	li x12, RCC_SYSPLL_SEL_PLL
	or x11, x11, x12
	sw x11, R32_RCC_PLLCFGR(x10)

PLLSRC_SEL:
	lw x11, R32_RCC_PLLCFGR(x10)
	andi x11, x11, ~(RCC_PLLSRC_MASK)
	ori x11, x11, RCC_PLLSRC_HSI
	sw x11, R32_RCC_PLLCFGR(x10)

PLLSRC_DIV:
	lw x11, R32_RCC_PLLCFGR(x10)
	li x12, ~(RCC_PLLSRC_DIV_MASK)
	and x11, x11, x12
	li x12, RCC_PLLSRC_1DIV
	or x11, x11, x12
	sw x11, R32_RCC_PLLCFGR(x10)

PLLMUL:
	lw x11, R32_RCC_PLLCFGR(x10)
	andi x11, x11, ~(RCC_PLLMUL_MASK)
	ori x11, x11, RCC_PLLMUL_16MUL
	sw x11, R32_RCC_PLLCFGR(x10)

PLLON:
	lw x11, R32_RCC_CTLR(x10)
	li x12, RCC_PLLON
	or x11, x11, x12
	sw x11, R32_RCC_CTLR(x10)
WAIT_PLLRDY:
	lw x11, R32_RCC_CTLR(x10)
	li x12, RCC_PLLRDY
	and x11, x11, x12
	beqz x11, WAIT_PLLRDY

SYSPLL_GATE_ON:
	lw x11, R32_RCC_PLLCFGR(x10)
	li x12, RCC_SYSPLL_GATE
	or x11, x11, x12
	sw x11, R32_RCC_PLLCFGR(x10)

SET_SYSCLK_PLL:
	lw x11, R32_RCC_CFGR0(x10)
	andi x11, x11, ~(RCC_SW_MASK)
	ori x11, x11, RCC_SW_PLL
	sw x11, R32_RCC_CFGR0(x10)
WAIT_SYSCLK_PLL:
	lw x11, R32_RCC_CFGR0(x10)
	andi x11, x11, RCC_SWS_MASK
	xori x11, x11, RCC_SWS_PLL
	bnez x11, WAIT_SYSCLK_PLL

DEASSERT_GPIOA_RST:
	lw x11, R32_RCC_HB2PRSTR(x10)
	andi x11, x11, ~(RCC_RST_IOPA)
	sw x11, R32_RCC_HB2PRSTR(x10)

GPIOA_CLK_ON:
	lw x11, R32_RCC_HB2PCENR(x10)
	ori x11, x11, RCC_CLKEN_IOPA
	sw x11, R32_RCC_HB2PCENR(x10)

DEASSERT_AFIO_RST:
	lw x11, R32_RCC_HB2PRSTR(x10)
	andi x11, x11, ~(RCC_RST_AFIO)
	sw x11, R32_RCC_HB2PRSTR(x10)

AFIO_CLK_ON:
	lw x11, R32_RCC_HB2PCENR(x10)
	ori x11, x11, RCC_CLKEN_AFIO
	sw x11, R32_RCC_HB2PCENR(x10)

DEASSERT_USART1_RST:
	lw x11, R32_RCC_HB2PRSTR(x10)
	li x12, ~(RCC_RST_USART1)
	and x11, x11, x12
	sw x11, R32_RCC_HB2PRSTR(x10)

USART1_CLK_ON:
	lw x11, R32_RCC_HB2PCENR(x10)
	li x12, RCC_CLKEN_USART1
	or x11, x11, x12
	sw x11, R32_RCC_HB2PCENR(x10)	

	li x10, GPIOA_BASE

USART1_TXP_CFG:
	lw x11, R32_GPIO_CFGHR(x10)
	andi x11, x11, ~(GPIO_MODE_MASK << 4)
	ori x11, x11, (GPIO_MODE_MUPPOUT << 4)
	sw x11, R32_GPIO_CFGHR(x10)

USART1_RXP_CFG:
	lw x11, R32_GPIO_CFGHR(x10)
	li x12, ~(GPIO_MODE_MASK << 8)
	and x11, x11, x12
	ori x11, x11, (GPIO_MODE_FLOATIN << 8)
	sw x11, R32_GPIO_CFGHR(x10)

	li x10, AFIO_BASE
USART1_TXP_MUXCFG:
	lw x11, R32_AFIO_GPIOA_AFHR(x10)
	andi x11, x11, ~(AFIO_AF_MASK << 4)
	ori x11, x11, (7 << 4)
	sw x11, R32_AFIO_GPIOA_AFHR(x10)
	
USART1_RXP_MUXCFG:
	lw x11, R32_AFIO_GPIOA_AFHR(x10)
	li x12, ~(AFIO_AF_MASK << 8)
	and x11, x11, x12
	ori x11, x11, (7 << 8)
	sw x11, R32_AFIO_GPIOA_AFHR(x10)

	li x10, USART1_BASE
USART1_CFG_RST:
	sw x0, R32_USART_CTLR1(x10)
	sw x0, R32_USART_CTLR2(x10)
	sw x0, R32_USART_CTLR3(x10)
	
USART1_BAUD_CFG:
	# SYSCLK CLOCK SOURCE IS PLL 400Mhz
	# HCLK CLOCK SOURCE IS SYSCLK / 4 100Mhz
	# UART CLOCK SOURCE IS HCLK 100Mhz
	# (100 * 1000 * 1000) / 16 / 115200 = 54.25347222222222
	# 4 / 16 = 0.25
	# (100 * 1000 * 1000) / (54.25 * 16) = 115207.3732718894
	.SET BAUD_115200, (54 << 4) | (4)
	li x11, BAUD_115200
	sw x11, R32_USART_BRR(x10)

USART1_TRXEN:
	lw x11, R32_USART_CTLR1(x10)
	ori x11, x11, (USART_TE | USART_RE)
	sw x11, R32_USART_CTLR1(x10)

USART1_UEN:
	lw x11, R32_USART_CTLR1(x10)
	li x12, USART_UE
	or x11, x11, x12
	sw x11, R32_USART_CTLR1(x10)

	.PUSHSECTION .text.unused
USART1_TEST_UUU:
1:	
	lw x11, R32_USART_STATR(x10)
	andi x11, x11, USART_TC
	beqz x11, 1b
	li x11, 0x55
	sw x11, R32_USART_DATAR(x10)
	j 1b
	.POPSECTION

	.PUSHSECTION .text.unused
USART1_TEST_ECHO:
1:
	lw x11, R32_USART_STATR(x10)
	andi x11, x11, USART_RXNE
	beqz x11, 1b
	lw x12, R32_USART_DATAR(x10)

2:
	lw x11, R32_USART_STATR(x10)
	andi x11, x11, USART_TC
	beqz x11, 2b
	sw x12, R32_USART_DATAR(x10)

	j USART1_TEST_ECHO
	.POPSECTION

	ret
	.POPSECTION
